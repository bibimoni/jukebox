//! Proto round-trip tests for the Stage-3 sidecar command extensions:
//! create_playlist, add_playlist_items, get_liked_songs, get_artist,
//! get_song_related, get_album.
//!
//! Each command has a request-serialization test (the wire line the sidecar
//! receives) and a response-parsing test (the wire line the sidecar sends
//! back). The wire format is `{"ok":true,"data":{"key":value}}` for responses
//! and `{"cmd":"snake_case_name",...}` for requests — matching the existing
//! proto tests in `tests/yt_sidecar.rs`.

use jukebox::yt::proto::*;

// ---------------------------------------------------------------------------
// create_playlist
// ---------------------------------------------------------------------------

#[test]
fn create_playlist_request_serializes() {
    let req = Request::CreatePlaylist {
        title: "My Mix".into(),
        description: "Generated by jukebox".into(),
        privacy: "PRIVATE".into(),
        video_ids: vec!["abc123".into(), "def456".into()],
    };
    let line = req.to_line();
    assert!(line.contains("\"cmd\":\"create_playlist\""));
    assert!(line.contains("\"title\":\"My Mix\""));
    assert!(line.contains("\"privacy\":\"PRIVATE\""));
    assert!(line.contains("\"video_ids\":[\"abc123\",\"def456\"]"));
    assert!(!line.contains('\n')); // single line
}

#[test]
fn create_playlist_request_minimal() {
    // Empty description + empty video_ids + empty privacy (serde defaults).
    let req = Request::CreatePlaylist {
        title: "T".into(),
        description: String::new(),
        privacy: String::new(),
        video_ids: Vec::new(),
    };
    let line = req.to_line();
    assert!(line.contains("\"cmd\":\"create_playlist\""));
    assert!(line.contains("\"title\":\"T\""));
}

#[test]
fn create_playlist_response_parses() {
    let line = r#"{"ok":true,"data":{"created_playlist":{"id":"PL123","title":"My Mix","privacy":"PRIVATE"}}}"#;
    let resp = Response::from_line(line).unwrap();
    match resp {
        Response::CreatedPlaylist { id, title, privacy } => {
            assert_eq!(id, "PL123");
            assert_eq!(title, "My Mix");
            assert_eq!(privacy, "PRIVATE");
        }
        other => panic!("expected CreatedPlaylist, got {other:?}"),
    }
}

#[test]
fn create_playlist_response_missing_fields_default() {
    // Missing fields → serde defaults (empty strings).
    let line = r#"{"ok":true,"data":{"created_playlist":{"id":"PL1"}}}"#;
    let resp = Response::from_line(line).unwrap();
    match resp {
        Response::CreatedPlaylist { id, title, privacy } => {
            assert_eq!(id, "PL1");
            assert_eq!(title, "");
            assert_eq!(privacy, "");
        }
        other => panic!("expected CreatedPlaylist, got {other:?}"),
    }
}

