#[test]
fn error_response_for_new_command() {
    // A sidecar error for any new command surfaces as Response::Error.
    let line = r#"{"ok":false,"error":"create_playlist failed: not authenticated"}"#;
    let resp = Response::from_line(line).unwrap();
    assert!(matches!(resp, Response::Error(e) if e.contains("create_playlist")));
}
